/**
 * Êó•ÂøóÊü•ÁúãÂô®ÁªÑ‰ª∂
 * ÊòæÁ§∫ÂÆûÊó∂Êó•ÂøóÊµÅÔºåÊîØÊåÅËøáÊª§ÂíåÊêúÁ¥¢
 */

import React, { useState, useEffect, useRef, useMemo } from 'react';
import { logger, LogLevel, LogEntry } from '@/utils/logger';
import { Input } from '@/components/ui/Input';
import { Button } from '@/components/ui/Button';
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from '@/components/ui/Select';
import { Card } from '@/components/ui/Card';
import { Badge } from '@/components/ui/Badge';
import { Switch } from '@/components/ui/Switch';
import { Label } from '@/components/ui/Label';
import { Copy, Download, Trash2, RefreshCw, ChevronUp, ChevronDown, Filter, FileText, FileJson, FileSpreadsheet } from 'lucide-react';
import { toast } from 'sonner';
import { save } from '@tauri-apps/plugin-dialog';
import { writeTextFile } from '@tauri-apps/plugin-fs';

const LogViewer: React.FC = () => {
  const [logs, setLogs] = useState<LogEntry[]>([]);
  const [filterLevel, setFilterLevel] = useState<LogLevel>(LogLevel.DEBUG);
  const [searchQuery, setSearchQuery] = useState('');
  const [autoScroll, setAutoScroll] = useState(true);
  const scrollRef = useRef<HTMLDivElement>(null);
  const [isPaused, setIsPaused] = useState(false);
  const [useRegex, setUseRegex] = useState(false);
  const [highlightSearch, setHighlightSearch] = useState(true);
  const [timeFilter, setTimeFilter] = useState<'all' | '1h' | '30m' | '5m'>('all');
  const [currentMatchIndex, setCurrentMatchIndex] = useState(0);
  const [showAdvancedFilters, setShowAdvancedFilters] = useState(false);
  const [showExportMenu, setShowExportMenu] = useState(false);
  const [selectedLogs, setSelectedLogs] = useState<Set<string>>(new Set());

  // Âä†ËΩΩÊó•Âøó
  const loadLogs = () => {
    let allLogs = logger.getFilteredLogs(filterLevel);

    // Êó∂Èó¥ËøáÊª§
    if (timeFilter !== 'all') {
      const now = Date.now();
      const timeMap = {
        '5m': 5 * 60 * 1000,
        '30m': 30 * 60 * 1000,
        '1h': 60 * 60 * 1000,
      };
      const timeLimit = timeMap[timeFilter];
      allLogs = allLogs.filter(log => now - log.timestamp.getTime() < timeLimit);
    }

    // ÊêúÁ¥¢ËøáÊª§
    if (searchQuery && searchQuery.trim()) {
      if (useRegex) {
        try {
          const regex = new RegExp(searchQuery, 'i');
          allLogs = allLogs.filter(log =>
            regex.test(log.message) || regex.test(JSON.stringify(log.args))
          );
        } catch (e) {
          // Ê≠£ÂàôË°®ËææÂºèÊó†ÊïàÔºå‰ΩøÁî®ÊôÆÈÄöÊêúÁ¥¢
          const searchLower = searchQuery.toLowerCase();
          allLogs = allLogs.filter(log =>
            log.message.toLowerCase().includes(searchLower) ||
            JSON.stringify(log.args).toLowerCase().includes(searchLower)
          );
        }
      } else {
        const searchLower = searchQuery.toLowerCase();
        allLogs = allLogs.filter(log =>
          log.message.toLowerCase().includes(searchLower) ||
          JSON.stringify(log.args).toLowerCase().includes(searchLower)
        );
      }
    }

    setLogs(allLogs);
    setCurrentMatchIndex(0);
  };

  // ÂàùÂßãÂä†ËΩΩÂíåÂÆöÊó∂Âà∑Êñ∞
  useEffect(() => {
    loadLogs();

    if (!isPaused) {
      const interval = setInterval(loadLogs, 1000); // ÊØèÁßíÂà∑Êñ∞
      return () => clearInterval(interval);
    }
  }, [filterLevel, searchQuery, isPaused, useRegex, timeFilter]);

  // Ëá™Âä®ÊªöÂä®Âà∞Â∫ïÈÉ®
  useEffect(() => {
    if (autoScroll && scrollRef.current) {
      scrollRef.current.scrollTop = scrollRef.current.scrollHeight;
    }
  }, [logs, autoScroll]);

  // Â§çÂà∂ÊâÄÊúâÊó•Âøó
  const handleCopyAll = () => {
    const text = logs.map(log => 
      `[${log.timestamp.toLocaleString()}] [${log.level}] ${log.message}${log.args.length > 0 ? ` ${  JSON.stringify(log.args)}` : ''}`
    ).join('\n');
    
    navigator.clipboard.writeText(text);
    toast.success('Êó•ÂøóÂ∑≤Â§çÂà∂Âà∞Ââ™Ë¥¥Êùø');
  };

  // ÂØºÂá∫‰∏∫ÊñáÊú¨Ê†ºÂºè
  const exportAsText = async () => {
    const logsToExport = selectedLogs.size > 0
      ? logs.filter(log => selectedLogs.has(log.id))
      : logs;

    const text = logsToExport.map(log =>
      `[${log.timestamp.toLocaleString()}] [${log.level}] ${log.message}${log.args.length > 0 ? ` ${  JSON.stringify(log.args)}` : ''}`
    ).join('\n');

    try {
      const filePath = await save({
        defaultPath: `logs-${new Date().toISOString().replace(/:/g, '-')}.txt`,
        filters: [{
          name: 'Text Files',
          extensions: ['txt']
        }]
      });

      if (filePath) {
        await writeTextFile(filePath, text);
        toast.success(`Â∑≤ÂØºÂá∫ ${logsToExport.length} Êù°Êó•ÂøóÂà∞ÊñáÊú¨Êñá‰ª∂`);
      }
    } catch (error) {
      console.error('ÂØºÂá∫Â§±Ë¥•:', error);
      toast.error('ÂØºÂá∫Â§±Ë¥•');
    }
  };

  // ÂØºÂá∫‰∏∫JSONÊ†ºÂºè
  const exportAsJSON = async () => {
    const logsToExport = selectedLogs.size > 0
      ? logs.filter(log => selectedLogs.has(log.id))
      : logs;

    const jsonData = logsToExport.map(log => ({
      timestamp: log.timestamp.toISOString(),
      level: log.level,
      message: log.message,
      args: log.args,
      stack: log.stack,
      source: log.source,
    }));

    try {
      const filePath = await save({
        defaultPath: `logs-${new Date().toISOString().replace(/:/g, '-')}.json`,
        filters: [{
          name: 'JSON Files',
          extensions: ['json']
        }]
      });

      if (filePath) {
        await writeTextFile(filePath, JSON.stringify(jsonData, null, 2));
        toast.success(`Â∑≤ÂØºÂá∫ ${logsToExport.length} Êù°Êó•ÂøóÂà∞JSONÊñá‰ª∂`);
      }
    } catch (error) {
      console.error('ÂØºÂá∫Â§±Ë¥•:', error);
      toast.error('ÂØºÂá∫Â§±Ë¥•');
    }
  };

  // ÂØºÂá∫‰∏∫CSVÊ†ºÂºè
  const exportAsCSV = async () => {
    const logsToExport = selectedLogs.size > 0
      ? logs.filter(log => selectedLogs.has(log.id))
      : logs;

    const headers = ['Timestamp', 'Level', 'Message', 'Args', 'Source'];
    const csvRows = [
      headers.join(','),
      ...logsToExport.map(log => [
        `"${log.timestamp.toISOString()}"`,
        `"${log.level}"`,
        `"${log.message.replace(/"/g, '""')}"`,
        `"${JSON.stringify(log.args).replace(/"/g, '""')}"`,
        `"${log.source || ''}"`,
      ].join(','))
    ];

    try {
      const filePath = await save({
        defaultPath: `logs-${new Date().toISOString().replace(/:/g, '-')}.csv`,
        filters: [{
          name: 'CSV Files',
          extensions: ['csv']
        }]
      });

      if (filePath) {
        await writeTextFile(filePath, csvRows.join('\n'));
        toast.success(`Â∑≤ÂØºÂá∫ ${logsToExport.length} Êù°Êó•ÂøóÂà∞CSVÊñá‰ª∂`);
      }
    } catch (error) {
      console.error('ÂØºÂá∫Â§±Ë¥•:', error);
      toast.error('ÂØºÂá∫Â§±Ë¥•');
    }
  };

  // Ê∏ÖÁ©∫Êó•Âøó
  const handleClear = () => {
    logger.clearLogs();
    setLogs([]);
    setSelectedLogs(new Set());
    toast.success('Êó•ÂøóÂ∑≤Ê∏ÖÁ©∫');
  };

  // ÂàáÊç¢Êó•ÂøóÈÄâÊã©
  const toggleLogSelection = (logId: string) => {
    const newSelected = new Set(selectedLogs);
    if (newSelected.has(logId)) {
      newSelected.delete(logId);
    } else {
      newSelected.add(logId);
    }
    setSelectedLogs(newSelected);
  };

  // ÂÖ®ÈÄâ/ÂèñÊ∂àÂÖ®ÈÄâ
  const toggleSelectAll = () => {
    if (selectedLogs.size === logs.length) {
      setSelectedLogs(new Set());
    } else {
      setSelectedLogs(new Set(logs.map(log => log.id)));
    }
  };

  // Ëé∑ÂèñÊó•ÂøóÁ∫ßÂà´È¢úËâ≤
  const getLevelColor = (level: string) => {
    switch (level) {
      case 'ERROR': return 'destructive';
      case 'WARN': return 'warning';
      case 'INFO': return 'default';
      case 'DEBUG': return 'secondary';
      default: return 'default';
    }
  };

  // Ëé∑ÂèñÊó•ÂøóÁ∫ßÂà´ÂõæÊ†á
  const getLevelIcon = (level: string) => {
    switch (level) {
      case 'ERROR': return '‚ùå';
      case 'WARN': return '‚ö†Ô∏è';
      case 'INFO': return '‚ÑπÔ∏è';
      case 'DEBUG': return 'üîç';
      default: return 'üìù';
    }
  };

  // È´ò‰∫ÆÊêúÁ¥¢ÂÖ≥ÈîÆËØç
  const highlightText = (text: string) => {
    if (!highlightSearch || !searchQuery || !searchQuery.trim()) {
      return text;
    }

    if (useRegex) {
      try {
        const regex = new RegExp(`(${searchQuery})`, 'gi');
        const parts = text.split(regex);
        return parts.map((part, index) =>
          regex.test(part) ? (
            <mark key={index} className="bg-yellow-300 dark:bg-yellow-600">{part}</mark>
          ) : part
        );
      } catch (e) {
        return text;
      }
    } else {
      const searchLower = searchQuery.toLowerCase();
      const index = text.toLowerCase().indexOf(searchLower);
      if (index === -1) return text;

      return (
        <>
          {text.substring(0, index)}
          <mark className="bg-yellow-300 dark:bg-yellow-600">
            {text.substring(index, index + searchQuery.length)}
          </mark>
          {text.substring(index + searchQuery.length)}
        </>
      );
    }
  };

  // ÂØºËà™Âà∞‰∏ä‰∏Ä‰∏™ÂåπÈÖç
  const navigateToPrevMatch = () => {
    if (logs.length === 0) return;
    setCurrentMatchIndex((prev) => (prev > 0 ? prev - 1 : logs.length - 1));
  };

  // ÂØºËà™Âà∞‰∏ã‰∏Ä‰∏™ÂåπÈÖç
  const navigateToNextMatch = () => {
    if (logs.length === 0) return;
    setCurrentMatchIndex((prev) => (prev < logs.length - 1 ? prev + 1 : 0));
  };

  // Âø´ÈÄüËøáÊª§È¢ÑËÆæ
  const applyQuickFilter = (preset: 'errors' | 'recent' | 'all') => {
    switch (preset) {
      case 'errors':
        setFilterLevel(LogLevel.ERROR);
        setTimeFilter('all');
        break;
      case 'recent':
        setFilterLevel(LogLevel.DEBUG);
        setTimeFilter('5m');
        break;
      case 'all':
        setFilterLevel(LogLevel.DEBUG);
        setTimeFilter('all');
        break;
    }
  };

  return (
    <div className="flex flex-col h-full space-y-4">
      {/* Â∑•ÂÖ∑Ê†è */}
      <div className="flex flex-wrap items-center gap-3">
        {/* Âø´ÈÄüËøáÊª§È¢ÑËÆæ */}
        <div className="flex items-center gap-2">
          <Button
            variant="outline"
            size="sm"
            onClick={() => applyQuickFilter('errors')}
          >
            ‰ªÖÈîôËØØ
          </Button>
          <Button
            variant="outline"
            size="sm"
            onClick={() => applyQuickFilter('recent')}
          >
            ÊúÄËøë5ÂàÜÈíü
          </Button>
          <Button
            variant="outline"
            size="sm"
            onClick={() => applyQuickFilter('all')}
          >
            ÂÖ®ÈÉ®
          </Button>
        </div>

        {/* È´òÁ∫ßËøáÊª§ÂºÄÂÖ≥ */}
        <Button
          variant="outline"
          size="sm"
          onClick={() => setShowAdvancedFilters(!showAdvancedFilters)}
        >
          <Filter className="w-4 h-4 mr-1" />
          {showAdvancedFilters ? 'ÈöêËóè' : 'ÊòæÁ§∫'}ËøáÊª§Âô®
        </Button>

        {/* Êìç‰ΩúÊåâÈíÆ */}
        <div className="flex items-center gap-2 ml-auto">
          <Button
            variant="outline"
            size="sm"
            onClick={() => setIsPaused(!isPaused)}
          >
            {isPaused ? 'ÁªßÁª≠' : 'ÊöÇÂÅú'}
          </Button>

          <Button
            variant="outline"
            size="sm"
            onClick={loadLogs}
          >
            <RefreshCw className="w-4 h-4" />
          </Button>

          <Button
            variant="outline"
            size="sm"
            onClick={handleCopyAll}
          >
            <Copy className="w-4 h-4 mr-1" />
            Â§çÂà∂
          </Button>

          {/* ÂØºÂá∫ËèúÂçï */}
          <div className="relative">
            <Button
              variant="outline"
              size="sm"
              onClick={() => setShowExportMenu(!showExportMenu)}
            >
              <Download className="w-4 h-4 mr-1" />
              ÂØºÂá∫ {selectedLogs.size > 0 && `(${selectedLogs.size})`}
            </Button>

            {showExportMenu && (
              <Card className="absolute right-0 top-full mt-2 z-50 p-2 min-w-[150px]">
                <div className="flex flex-col gap-1">
                  <Button
                    variant="ghost"
                    size="sm"
                    className="justify-start"
                    onClick={() => {
                      exportAsText();
                      setShowExportMenu(false);
                    }}
                  >
                    <FileText className="w-4 h-4 mr-2" />
                    ÊñáÊú¨Ê†ºÂºè (.txt)
                  </Button>
                  <Button
                    variant="ghost"
                    size="sm"
                    className="justify-start"
                    onClick={() => {
                      exportAsJSON();
                      setShowExportMenu(false);
                    }}
                  >
                    <FileJson className="w-4 h-4 mr-2" />
                    JSONÊ†ºÂºè (.json)
                  </Button>
                  <Button
                    variant="ghost"
                    size="sm"
                    className="justify-start"
                    onClick={() => {
                      exportAsCSV();
                      setShowExportMenu(false);
                    }}
                  >
                    <FileSpreadsheet className="w-4 h-4 mr-2" />
                    CSVÊ†ºÂºè (.csv)
                  </Button>
                </div>
              </Card>
            )}
          </div>

          <Button
            variant="outline"
            size="sm"
            onClick={handleClear}
          >
            <Trash2 className="w-4 h-4 mr-1" />
            Ê∏ÖÁ©∫
          </Button>
        </div>
      </div>

      {/* È´òÁ∫ßËøáÊª§Âô® */}
      {showAdvancedFilters && (
        <Card className="p-4">
          <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-4">
            {/* Êó•ÂøóÁ∫ßÂà´ */}
            <div className="space-y-2">
              <Label>Êó•ÂøóÁ∫ßÂà´</Label>
              <Select
                value={filterLevel.toString()}
                onValueChange={(value) => setFilterLevel(parseInt(value) as LogLevel)}
              >
                <SelectTrigger>
                  <SelectValue />
                </SelectTrigger>
                <SelectContent>
                  <SelectItem value={LogLevel.ERROR.toString()}>ERROR</SelectItem>
                  <SelectItem value={LogLevel.WARN.toString()}>WARN+</SelectItem>
                  <SelectItem value={LogLevel.INFO.toString()}>INFO+</SelectItem>
                  <SelectItem value={LogLevel.DEBUG.toString()}>ALL</SelectItem>
                </SelectContent>
              </Select>
            </div>

            {/* Êó∂Èó¥ËåÉÂõ¥ */}
            <div className="space-y-2">
              <Label>Êó∂Èó¥ËåÉÂõ¥</Label>
              <Select
                value={timeFilter}
                onValueChange={(value: any) => setTimeFilter(value)}
              >
                <SelectTrigger>
                  <SelectValue />
                </SelectTrigger>
                <SelectContent>
                  <SelectItem value="all">ÂÖ®ÈÉ®</SelectItem>
                  <SelectItem value="5m">ÊúÄËøë5ÂàÜÈíü</SelectItem>
                  <SelectItem value="30m">ÊúÄËøë30ÂàÜÈíü</SelectItem>
                  <SelectItem value="1h">ÊúÄËøë1Â∞èÊó∂</SelectItem>
                </SelectContent>
              </Select>
            </div>

            {/* ÊêúÁ¥¢ÈÄâÈ°π */}
            <div className="space-y-2">
              <Label>ÊêúÁ¥¢ÈÄâÈ°π</Label>
              <div className="flex items-center gap-4">
                <div className="flex items-center gap-2">
                  <Switch
                    id="regex-mode"
                    checked={useRegex}
                    onCheckedChange={setUseRegex}
                  />
                  <Label htmlFor="regex-mode" className="text-sm cursor-pointer">
                    Ê≠£ÂàôË°®ËææÂºè
                  </Label>
                </div>
                <div className="flex items-center gap-2">
                  <Switch
                    id="highlight-mode"
                    checked={highlightSearch}
                    onCheckedChange={setHighlightSearch}
                  />
                  <Label htmlFor="highlight-mode" className="text-sm cursor-pointer">
                    È´ò‰∫Æ
                  </Label>
                </div>
              </div>
            </div>

            {/* ÊêúÁ¥¢Ê°Ü */}
            <div className="space-y-2">
              <Label>ÊêúÁ¥¢</Label>
              <div className="flex items-center gap-2">
                <Input
                  placeholder={useRegex ? "Ê≠£ÂàôË°®ËææÂºè..." : "ÊêúÁ¥¢ÂÖ≥ÈîÆËØç..."}
                  value={searchQuery}
                  onChange={(e) => setSearchQuery(e.target.value)}
                  className="flex-1"
                />
                {searchQuery && logs.length > 0 && (
                  <div className="flex items-center gap-1">
                    <Button
                      variant="outline"
                      size="sm"
                      onClick={navigateToPrevMatch}
                    >
                      <ChevronUp className="w-4 h-4" />
                    </Button>
                    <Button
                      variant="outline"
                      size="sm"
                      onClick={navigateToNextMatch}
                    >
                      <ChevronDown className="w-4 h-4" />
                    </Button>
                  </div>
                )}
              </div>
            </div>
          </div>
        </Card>
      )}

      {/* ÁªüËÆ°‰ø°ÊÅØ */}
      <div className="flex items-center gap-4 text-sm text-muted-foreground">
        <span>ÊÄªËÆ°: {logs.length} Êù°</span>
        {selectedLogs.size > 0 && (
          <span>Â∑≤ÈÄâ: {selectedLogs.size} Êù°</span>
        )}
        {searchQuery && logs.length > 0 && (
          <span>ÂåπÈÖç: {currentMatchIndex + 1} / {logs.length}</span>
        )}
        {logs.length > 0 && (
          <Button
            variant="ghost"
            size="sm"
            onClick={toggleSelectAll}
          >
            {selectedLogs.size === logs.length ? 'ÂèñÊ∂àÂÖ®ÈÄâ' : 'ÂÖ®ÈÄâ'}
          </Button>
        )}
        <label className="flex items-center gap-2 cursor-pointer">
          <input
            type="checkbox"
            checked={autoScroll}
            onChange={(e) => setAutoScroll(e.target.checked)}
            className="rounded"
          />
          Ëá™Âä®ÊªöÂä®
        </label>
      </div>

      {/* Êó•ÂøóÂàóË°® */}
      <Card className="flex-1 overflow-hidden">
        <div ref={scrollRef} className="h-full overflow-y-auto p-4 space-y-2 font-mono text-sm">
          {logs.length === 0 ? (
            <div className="text-center text-muted-foreground py-8">
              ÊöÇÊó†Êó•Âøó
            </div>
          ) : (
            logs.map((log, index) => (
                <div
                  key={log.id}
                  className={`flex items-start gap-3 p-2 rounded hover:bg-accent/50 transition-colors ${
                    searchQuery && index === currentMatchIndex ? 'bg-accent border-2 border-primary' : ''
                  }`}
                >
                  {/* ÈÄâÊã©Ê°Ü */}
                  <input
                    type="checkbox"
                    checked={selectedLogs.has(log.id)}
                    onChange={() => toggleLogSelection(log.id)}
                    className="mt-1 rounded cursor-pointer"
                  />

                  {/* Êó∂Èó¥Êà≥ */}
                  <span className="text-muted-foreground whitespace-nowrap text-xs">
                    {log.timestamp.toLocaleTimeString()}
                  </span>

                  {/* Á∫ßÂà´ */}
                  <Badge variant={getLevelColor(log.level) as any} className="shrink-0">
                    {getLevelIcon(log.level)} {log.level}
                  </Badge>

                  {/* Ê∂àÊÅØ */}
                  <div className="flex-1 break-all">
                    <div>{highlightText(log.message)}</div>
                    {log.args.length > 0 && (
                      <div className="text-muted-foreground mt-1">
                        {highlightText(JSON.stringify(log.args, null, 2))}
                      </div>
                    )}
                    {log.stack && (
                      <details className="mt-1 text-xs text-muted-foreground">
                        <summary className="cursor-pointer">Â†ÜÊ†àË∑üË∏™</summary>
                        <pre className="mt-1 whitespace-pre-wrap">{log.stack}</pre>
                      </details>
                    )}
                  </div>
                </div>
              ))
            )}
        </div>
      </Card>
    </div>
  );
};

export default LogViewer;

